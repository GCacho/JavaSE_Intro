public class DataType {
    public static void main(String[] args) {
        // Var
        var variable = 2; // Se transforma en cualquier tipo de dato: 'k', "Hola Mundo", 2344, 345456.33;
        // Integers
        int n = 123;
        // Long
        long nL = 1238485848863L;
        // Double
        double nD = 1232434.34343455;
        // Float
        float nF = 34234554.454545F;
        // Char
        char c = 'h';
        // String
        String s = "Hola Mundooo!";
        // Bool
        boolean b = true; // false;
    }
}
